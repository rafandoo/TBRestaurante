package restaurante;

public class Controle {
	
	
	// Atributos
	private double total = 0;
	private int pessoasAtendidas = 0;
	private float valorDoBuffet = 0.00f;
	
	
	// Getters e Setters
	public double getTotal() {
		return total;
	}
	
	
	public void setTotal(double total) {
		this.total = total;
	}
	
	
	public int getPessoasAtendidas() {
		return pessoasAtendidas;
	}
	
	
	public void setPessoasAtendidas(int pessoasAtendidas) {
		this.pessoasAtendidas = pessoasAtendidas;
	}
	
	
	public float getValorDoBuffet() {
		return valorDoBuffet;
	}
	
	
	public void setValorDoBuffet(float valorDoBuffet) {
		this.valorDoBuffet = valorDoBuffet;
	}
	
}
